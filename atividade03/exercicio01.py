# Faça um Programa que peça dois números e imprima o maior deles.
numero01 = input("Digite o valor do primeiro número: ")
numero02 = input("Digite o valor do segundo número: ")

if numero01 > numero02:
    print("O número01,", numero01, "é maior que o número02", numero02)
else:
    print("O número02,", numero02, "é maior que o número01", numero01)
